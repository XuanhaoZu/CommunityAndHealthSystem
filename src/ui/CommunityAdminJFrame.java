    private void btnEditHospitalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditHospitalActionPerformed
        try {
            int selectedRowIndex =tblHospital.getSelectedRow();
        hospitalmodel = (DefaultTableModel) tblHospital.getModel();
        Hospital hospital = (Hospital)hospitalmodel.getValueAt(tblHospital.convertRowIndexToModel(selectedRowIndex), 0);
        CommunityAdminHospitalJPanel communityAdminHospitalJPanel = new CommunityAdminHospitalJPanel(communityAdmin, hospital);
        add(communityAdminHospitalJPanel);
        communityAdminHospitalJPanel.setVisible(true);
        startPanel.setVisible(false);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please select a hospital first");
        }

    }//GEN-LAST:event_btnEditHospitalActionPerformed

