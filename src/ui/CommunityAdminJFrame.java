    private void populateTableHouse() {
        housemodel = (DefaultTableModel) tblHouse.getModel();
        housemodel.setRowCount(0);
        
        for (House house : AllSystem.houses) {
            Object[] row = new Object[2];
            row[0] = house;
            row[1] = house.getOwner();
            
            housemodel.addRow(row);
        }
    }
    
    private void populateTableHospital() {
        hospitalmodel = (DefaultTableModel) tblHospital.getModel();
        hospitalmodel.setRowCount(0);
        
        for (Hospital hospital : AllSystem.hospitals) {
            Object[] row = new Object[1];
            row[0] = hospital;
           
            
            hospitalmodel.addRow(row);
        }
        
    }


