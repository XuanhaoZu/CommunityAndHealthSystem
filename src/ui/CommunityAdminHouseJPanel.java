    private void populateTable() {
        model = (DefaultTableModel) tblPerson.getModel();
        model.setRowCount(0);
        for (Person person : house.getPersons()) {
            Object[] row = new Object[1];
            row[0] = person;
            
            model.addRow(row);
        
    }
    }
    

